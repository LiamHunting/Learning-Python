#Fundamental Data Types

#int and float
print(2 + 4) #6
print(2 - 4) #-2
print(2 * 4) #8
print(2 / 4) #0.5

print(type(2 + 4)) #int
print(type(2 - 4)) #int
print(type(2 * 4)) #int
print(type(2 / 4)) #float

print(type(9.9 + 1.1)) #float equal to 11.0

print(2 ** 3) #2 to the power of 3 = 8
print(5 // 4) #division rounded down to an integer
print(5 % 4) #divides and gets the remainder

#math functions
print(round(3.9)) #rounds to the nearest whole int
print(abs(-30)) #show the absolute value
